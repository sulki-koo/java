package javabasic;

public class Variable3 {

	// access modifier (접근제한자=접근지정자=접근한정자)
	// public, protected, default, private
	
	// public : 모든 패키지에서 접근 가능
	public int pubi = 1;
	// protected : 동일한 패키지 또는 상속관계에 있는 패키지에서 접근 가능
	protected int proti = 2;
	// default : 동일한 패키지에서만 접근 가능
	int di = 3; // default
	// private : 클래스내에서만 접근 가능
	private int prii = 4;
	
	
}
